#include "libft.h"

void	ft_memdel(void **ap)
{
	size_t i;

	if (!ap)
		return ;
	i = 0;
	while (((char**)ap)[i] != '\0')
	{
		free(ap[0]);
		ap[0] = NULL;
	}
}
